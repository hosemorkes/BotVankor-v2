                report = format_weather_report(weather_data)
                await query.edit_message_text(report)
                logger.info(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {query.from_user.id} –∑–∞–ø—Ä–æ—Å–∏–ª –ø–æ–≥–æ–¥—É —á–µ—Ä–µ–∑ –∫–Ω–æ–ø–∫—É")
